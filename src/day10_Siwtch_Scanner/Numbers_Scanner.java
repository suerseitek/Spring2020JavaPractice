public class Numbers_Scanner {
    public static void main(String[] args) {

        switch(4){
            case 1:
                System.out.println("One");
       break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("three");
                break;
            case 4:
                System.out.println("four");
                break;

            default:
                System.out.println("Invalid case");
                break;

        }












    }
}
