package day05_Unary_ShorthandOperators;

public class UnaryOperators {
    public static void main(String[] args) {

        int a = 100;

        System.out.println(++a); //increment, increase the value by 1

        int b = 200;

        System.out.println(b++); // 100, first passe the current value
        System.out.println(b); // increases the value by 1




    }


}

