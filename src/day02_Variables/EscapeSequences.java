package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\tHello");
    // \t means a paragraph space

        System.out.println("\nCybertek School");
        System.out.println("Cybertek\nSchool");
        System.out.println("\nMy\nname\nis\nSeitek");
// \n new line


        System.out.println("My favorite TV show is \"Games of Thrones\"");
// \" prints double quote

        System.out.println("\\ ");
        // \\ prints single \ on console
        System.out.println("/");

    }
}
